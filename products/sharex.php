                    </a>
                  </h5>
                </div>
                <div id="faq07" class="panel-collapse collapse">
                  <div class="panel-body">
                    <p>It's common knowledge that password policies are often circumvented in daily use. That's because passwords for serious business applications are so complex that they are difficult to remember. To cope, users react by posting passwords on sticky notes for all to see, or if in a group setting, passwords are often emailed around for shared use. These practices expose the organization to serious risk while frustrating the user. The ShareX pin-code login addresses this issue. A pin-code, tied to the device creates the security of two-factor authentication, while the four-digits in the pin-code are a comparatively easy thing to remember. This makes for an architecture that is at once more secure while delivering a more user-friendly experience.</p>
