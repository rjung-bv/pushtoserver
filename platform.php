            <p>&nbsp;</p>
            <h4>Producer-Consumer Model</h4>
            <p>A visual representation makes complex information actionable. That’s why the platform places an emphasis on graphics and animation throughout its design. Visual design will offset the screen size limitations of mobile devices, which is of particular importance in presenting the multi-layered documents typically found in and around the executive suite.</p>
            <p>&nbsp;</p>
            <h4>Enterprise Sync</h4>
            <p>For syncing the platform uses an Enterprise Sync architecture, which decouples the upload function from the sharing function. Files may be shared only after consumer permissions have been set. However once initialized, updates are auto-synced. This approach provides the popular benefit of instant access to last-minute changes but avoids the risk of inadvertent sharing with unauthorized users, an ever-present risk in Desktop Sync.</p>
            <p>&nbsp;</p>
            <h4>Content Segregation</h4>
            <p>The platform architecture segregates content in separate portals which function as focal points for team initiatives or processes. Because these portals are ring-fenced for security, consumers only have access to the content for which they are explicitly authorized, thereby reducing the risk of sharing outside the team. At the same time, its user-friendly design allows authorized consumers to switch between portals without the burden of entering additional passwords.</p>
            <p>&nbsp;</p>
            <h4>Remote Purge</h4>
            <p>Not unreasonably, consumers expect ready access to important materials even if they're out of Wi-Fi range. But there is a tension between this newfound mobility and good security, particularly with downloaded materials. To bridge this gap, the platform uses the Briefcase, an encrypted folder, which functions so that any content, whether viewed online or offline, remains under control of the producer. Even if a consumer neglects to delete downloaded documents a producer can manage that task, and purge all the content, including the notes, remotely.</p>
            <p>By exercising control over every aspect of the collaboration chain, the platform reserves the benefits of file sharing and ready access to updated content, without the worry of information leaks.</p>
